// Get districts by division/region
export const getDistricts = (division) => {
  const districts = warehouses
    .filter(w => w.region === division && w.status === 'active')
    .map(w => w.district);
  return [...new Set(districts)]; // Remove duplicates
};

// Get cities by division and district
export const getCities = (division, district) => {
  const warehouse = warehouses.find(
    w => w.region === division && w.district === district && w.status === 'active'
  );
  return warehouse ? [warehouse.city] : [];
};

// Get covered areas by division, district, and city
export const getAreas = (division, district, city) => {
  const warehouse = warehouses.find(
    w => w.region === division && w.district === district && w.city === city && w.status === 'active'
  );
  return warehouse ? warehouse.covered_area : [];
};

